CREATE TABLE STUDENTS_ENROLLED_TO_COURSES
(
    STUDENT_ID VARCHAR(32),
    COURSE_ID  VARCHAR(32),
    PRIMARY KEY (STUDENT_ID, COURSE_ID)
);
CREATE TABLE STUDENTS
(
    ID              VARCHAR(32),
    NAME            VARCHAR(50),
    SURNAME         VARCHAR(50),
    DATE_OF_BIRTH   DATE,
    PHONE_NUMBER    VARCHAR(9),
    STUDENT_CARD_ID VARCHAR(6),
    PRIMARY KEY (ID)
);
